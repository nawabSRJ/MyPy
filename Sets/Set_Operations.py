# * Sets can be used to carry out mathematical set operations like union, intersection, difference and symmetric difference. We can do this with operators or methods.

# todo - consider 2 sets
a = {1,2,3,4,5}
b = {4,5,6,7,8}

print(a.union(b))   # * UNION Operation
print(a|b)


print(a.intersection(b))    # * INTERSECTION Operation
print(a&b)


print(a-b)      # * MINUS Operation

print(a^b)

