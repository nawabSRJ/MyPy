import statistics 
from statistics import mean, median, mode
# * you can write any of the above , 2nd one is used to import specific functions

seq = [5,10,15,5,30,40]

print(statistics.mean(seq)) # * mean ~ average
print(statistics.median(seq))   # * median ~ middle value in sequence
print(statistics.mode(seq))     # * mode ~ smallest value in sequence





